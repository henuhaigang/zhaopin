package com.haigang.domain;
/**
 * 工作类型（如：IT，互联网等等）
 * @author haigang
 *
 */
public class WorkMark {
	
	private long id;
	
	private String name;

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	

}
